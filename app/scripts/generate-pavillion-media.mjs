import { readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const appRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const mediaDirectory = path.join(appRoot, 'public', 'pavillion');
const outputFile = path.join(appRoot, 'src', 'data', 'pavillion', 'media.generated.ts');
const supportedExtensions = new Set(['.gif', '.jpeg', '.jpg', '.png', '.webp']);

const files = (await readdir(mediaDirectory, { withFileTypes: true }))
  .filter((entry) => entry.isFile() && supportedExtensions.has(path.extname(entry.name).toLowerCase()))
  .map((entry) => entry.name);

const groups = new Map();
const ungrouped = [];

for (const file of files) {
  const match = file.match(/^(.*?)_v(\d+)\.[^.]+$/i);

  if (!match) {
    ungrouped.push(`/pavillion/${file}`);
    continue;
  }

  const [, groupName, version] = match;
  const group = groups.get(groupName) ?? [];
  group.push({ version: Number(version), src: `/pavillion/${file}` });
  groups.set(groupName, group);
}

const mediaGroups = Object.fromEntries(
  [...groups.entries()]
    .sort(([left], [right]) => left.localeCompare(right))
    .map(([groupName, group]) => [
      groupName,
      group.sort((left, right) => left.version - right.version).map((item) => item.src),
    ]),
);

const generatedSource = `// This file is generated by scripts/generate-pavillion-media.mjs.
// Add media to public/pavillion, then run npm run media:sync.

export const pavillionMedia = ${JSON.stringify(mediaGroups, null, 2)} as const;

export const pavillionUngroupedMedia = ${JSON.stringify(ungrouped.sort(), null, 2)} as const;
`;

await writeFile(outputFile, generatedSource, 'utf8');

const groupedFileCount = Object.values(mediaGroups).reduce((total, group) => total + group.length, 0);
console.log(`[Pavillion] Synced ${groupedFileCount} grouped and ${ungrouped.length} ungrouped media files.`);
